const express = require('express');
const { bodyLimit } = require('./config/env');
const requestId = require('./middleware/request-id');
const errorHandler = require('./middleware/error-handler');
const { createRouter } = require('./routes');
const citizenRequests = require('./modules/citizen-requests/router');
const oirs = require('./modules/oirs/router');
const auth = require('./modules/auth/router');
const users = require('./modules/users/router');
const departments = require('./modules/departments/router');
const procedures = require('./modules/procedures/router');
const documents = require('./modules/documents');
const expedients = require('./modules/expedients/router');
const correspondence = require('./modules/correspondence/router');
const reports = require('./modules/reports/router');
const notifications = require('./modules/notifications/router');
const externalEntities = require('./modules/external-entities/router');
const publicContent = require('./modules/public-content/router');
function createApp() { const app = express(); app.disable('x-powered-by'); app.use(express.json({ limit: bodyLimit, type: 'application/json' })); app.use(requestId); app.use(createRouter()); app.use(auth); app.use(users); app.use(departments); app.use(procedures); app.use(externalEntities); app.use(documents); app.use(citizenRequests); app.use(oirs); app.use(publicContent); app.use(reports); app.use(notifications); app.use(expedients); app.use(correspondence); app.use(errorHandler); return app; }
module.exports = { createApp };
